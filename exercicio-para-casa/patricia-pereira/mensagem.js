export function mensagemBoasVindas(nome) {
    return "Olá, " + nome + "! Seja bem vinda!"
}

export function mensagemApresentacao(nome, idade, cidade) {
    return "Eu me chamo " + nome + ", tenho " + idade + " e moro em " + cidade
}

export function mensagemAgradecimento (nome, produto) {
    return nome + ", obrigada por comprar nosso " + produto + "! Volte sempre!"
}
